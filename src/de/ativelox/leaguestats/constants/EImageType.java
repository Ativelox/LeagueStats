package de.ativelox.leaguestats.constants;

/**
 * Provides enums to indicate what sort of image to fetch from the data server.
 *
 * @author Ativelox {@literal <ativelox.dev@web.de>}
 *
 */
public enum EImageType {

	/**
	 * Corresponds to abilities.
	 */
	ABILITY,

	/**
	 * Corresponds to champions.
	 */
	CHAMPION,

	/**
	 * Referring to Shop-Items ingame.
	 */
	ITEM,

	/**
	 * Corresponds to maps.
	 */
	MAP,

	/**
	 * Corresponds to masteries.
	 */
	MASTERY,

	/**
	 * Corresponds to passive abilities.
	 */
	PASSIVEABILITY,

	/**
	 * Corresponds to profile icons.
	 */
	PROFILEICON,

	/**
	 * Corresponds to runes.
	 */
	RUNE,

	/**
	 * Corresponds to summoner spells.
	 */
	SUMMONERSPELL;
}
